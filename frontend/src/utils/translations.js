// Helper function to get translated text
export const translate = (key, language = 'english') => {
    const langDict = translations[language] || translations.english;
    return langDict[key] || translations.english[key] || key;
};

// Helper function to translate commodity names
export const translateCommodity = (commodityName, language = 'english') => {
    if (!commodityName) return '';

    // Check if we have a translation for this commodity
    if (commodityNames[commodityName]) {
        return commodityNames[commodityName][language] || commodityName;
    }

    // Return original name if no translation available
    return commodityName;
};
